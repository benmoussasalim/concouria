        return $this->addUsingAlias(SalePeer::DATE_CREATION, time() - $nbDays * 24 * 60 * 60, Criteria::GREATER_EQUAL);
    }

    /**Order by create date desc
     * @return     SaleQuery The current query, for fluid interface
     */
    public function lastCreatedFirst(){
        return $this->addDescendingOrderByColumn(SalePeer::DATE_CREATION);
    }

    /**Order by create date asc
     * @return     SaleQuery The current query, for fluid interface
     */
    public function firstCreatedFirst(){
        return $this->addAscendingOrderByColumn(SalePeer::DATE_CREATION);
    }
