    protected function findPkSimple($key, $con)
    {
        $sql = 'SELECT `id_concours`, `title`, `url`, `price`, `date`, `online`, `order`, `id_creation`, `id_modification` FROM `concours` WHERE `id_concours` = :p0';
        try {
            $stmt = $con->prepare($sql);
            $stmt->bindValue(':p0', $key, PDO::PARAM_INT);
            $stmt->execute();
        } catch (Exception $e) {
            Propel::log($e->getMessage(), Propel::LOG_ERR);
            throw new PropelException(sprintf('Unable to execute SELECT statement [%s]', $sql), $e);
        }
        $obj = null;
        if ($row = $stmt->fetch(PDO::FETCH_NUM)) {
            $obj = new Concours();
            $obj->hydrate($row);
            ConcoursPeer::addInstanceToPool($obj, (string) $key);
        }
        $stmt->closeCursor();

        return $obj;
    }

